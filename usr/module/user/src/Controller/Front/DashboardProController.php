class DashboardProController extends DashboardController
{
    public function indexAction()
    {
        $this->view()->headTitle(__('My Pro Dashboard'));
        parent::indexAction(); // TODO: Change the autogenerated stub

        // Get config
        $config = Pi::service('registry')->config->read('user');

        // Check dashboard active
        if (!$config['dashboard_enable']) {
            return $this->jump([
                'controller' => 'profile',
                'action'     => 'index',
            ]);
        }
    }

}
